/*
https://leetcode.com/problems/shortest-path-in-binary-matrix/

Given an n x n binary matrix grid, return the length of the shortest clear path in the matrix. If
there is no clear path, return -1.

A clear path in a binary matrix is a path from the top-left cell (i.e., (0, 0)) to the bottom-right
cell (i.e., (n - 1, n - 1)) such that:

    All the visited cells of the path are 0.
    All the adjacent cells of the path are 8-directionally connected (i.e., they are different and
they share an edge or a corner).

The length of a clear path is the number of visited cells of this path.


Example 1:

Input: grid = [[0,1],[1,0]]
Output: 2

Example 2:

Input: grid = [[0,0,0],[1,1,0],[1,1,0]]
Output: 4

Example 3:

Input: grid = [[1,0,0],[1,1,0],[1,1,0]]
Output: -1


Constraints:

    n == grid.length
    n == grid[i].length
    1 <= n <= 100
    grid[i][j] is 0 or 1
*/

#include "bits/stdc++.h"
#include <iostream>
using namespace std;

class Solution {
  public:
    int shortestPathBinaryMatrix(vector<vector<int>> &grid) {
        int n = grid.size();
        int res = -1;
        if (grid[0][0] == 1 or grid[n - 1][n - 1] == 1) {
            return -1;
        }

        vector<vector<bool>> visited = vector<vector<bool>>();
        vector<vector<int>> dist = vector<vector<int>>();
        for (int i = 0; i < n; ++i) {
            vector<bool> v_tmp(n, false);
            vector<int> d_tmp(n, 1000);
            visited.push_back(v_tmp);
            dist.push_back(d_tmp);
        }

        // BFS
        dist[0][0] = 1;
        queue<pair<pair<int, int>, int>> bfs_q;
        bfs_q.push({{0, 0}, dist[0][0]});
        while (!bfs_q.empty()) {
            pair<pair<int, int>, int> curr = bfs_q.front();
            bfs_q.pop();
            int curr_i = curr.first.first;
            int curr_j = curr.first.second;
            int curr_dist = curr.second;
            if (visited[curr_i][curr_j]) {
                continue;
            }

            visited[curr_i][curr_j] = true;
            dist[curr_i][curr_j] = min(dist[curr_i][curr_j], curr_dist);

            if (curr_i > 0 && grid[curr_i - 1][curr_j] == 0 && !visited[curr_i - 1][curr_j]) {
                bfs_q.push({{curr_i - 1, curr_j}, curr_dist + 1});
            }
            if (curr_i > 0 && curr_j > 0 && grid[curr_i - 1][curr_j - 1] == 0 &&
                !visited[curr_i - 1][curr_j - 1]) {
                bfs_q.push({{curr_i - 1, curr_j - 1}, curr_dist + 1});
            }
            if (curr_i > 0 && curr_j < n - 1 && grid[curr_i - 1][curr_j + 1] == 0 &&
                !visited[curr_i - 1][curr_j + 1]) {
                bfs_q.push({{curr_i - 1, curr_j + 1}, curr_dist + 1});
            }
            if (curr_j > 0 && grid[curr_i][curr_j - 1] == 0 && !visited[curr_i][curr_j - 1]) {
                bfs_q.push({{curr_i, curr_j - 1}, curr_dist + 1});
            }
            if (curr_j < n - 1 && grid[curr_i][curr_j + 1] == 0 && !visited[curr_i][curr_j + 1]) {
                bfs_q.push({{curr_i, curr_j + 1}, curr_dist + 1});
            }
            if (curr_i < n - 1 && grid[curr_i + 1][curr_j] == 0 && !visited[curr_i + 1][curr_j]) {
                bfs_q.push({{curr_i + 1, curr_j}, curr_dist + 1});
            }
            if (curr_i < n - 1 && curr_j > 0 && grid[curr_i + 1][curr_j - 1] == 0 &&
                !visited[curr_i + 1][curr_j - 1]) {
                bfs_q.push({{curr_i + 1, curr_j - 1}, curr_dist + 1});
            }
            if (curr_i < n - 1 && curr_j < n - 1 && grid[curr_i + 1][curr_j + 1] == 0 &&
                !visited[curr_i + 1][curr_j + 1]) {
                bfs_q.push({{curr_i + 1, curr_j + 1}, curr_dist + 1});
            }
        }

        if (dist[n - 1][n - 1] == 1000) {
            res = -1;
        } else {
            res = dist[n - 1][n - 1];
        }
        return res;
    }
};

